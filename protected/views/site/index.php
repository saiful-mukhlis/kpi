Yii::app ()->clientScript ->registerScript('slider',
"
  var \$carousel = \$('#myCarousel');
    var \$carouselCaptions = \$carousel.find('.item .carousel-caption');
    var \$carouselImages = \$carousel.find('.item img');
    var carouselTimeout;
    
    \$carousel.on('slid', function () {
        var \$item = \$carousel.find('.item.active');
        carouselTimeout = setTimeout(function() { // start the delay
            carouselTimeout = false;
            \$('.carousel-caption', \$item).animate({left: '5%',right: '0%',opacity: 1}, 1000);
            //\$('img', \$item).animate({'opacity': 0.8}, 100);
        }, 100);
    }).on('slide', function () {
        if(carouselTimeout) { // Carousel is sliding, stop pending animation if any
            clearTimeout(carouselTimeout);
            carouselTimeout = false;
        }
        // Reset styles
        \$carouselCaptions.animate({left: '20%',right: '0%',opacity: 0}, 10);
        //\$carouselImages.animate({'opacity': 1}, 100);
    });;
    
    \$('#myCarousel').carousel({
        interval: 6000,
        cycle: true,
    }).trigger('slid');			
			"
,CClientScript::POS_END);


